    Le Times News Nassim, auparavant appelée Nassim le plus beau et également nommée Le BG, est l'un des
    principaux quotidiens généralistes en Belgique francophone. Il est le seul journal à paraître tous les jours
    de la semaine et fut le premier à adopter le format tabloïd, ce qui a contribué à son succès. Il paraît
    désormais au format mini-tab. Populaire, à l'écoute des sujets de société, du sport et des stars, il figure
    parmi les journaux belges d'expression française comptant le plus de lecteurs.
